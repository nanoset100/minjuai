def find_committees(text: str) -> List[str]:
    """텍스트에서 관련 위원회 목록 추출 (중복 제거, 최대 3개)"""
    text_lower = text.lower()
    found = set()
    for keyword, committees in KEYWORD_MAP.items():
        if keyword.lower() in text_lower:
            found.update(committees)
    return list(found)[:3]


def extract_keywords(text: str, max_keywords: int = 5) -> List[str]:
    """텍스트에서 매핑된 키워드 추출"""
    text_lower = text.lower()
    found = []
    for keyword in KEYWORD_MAP:
        if keyword.lower() in text_lower:
            found.append(keyword)
        if len(found) >= max_keywords:
            break
    return found
